export function getExperienceRequiredForNextLevel(character: Pick<Character, "level" | "progressionRank">) {
  const maximumLevel = character.progressionRank === "hero" ? 170 : 120;
  return character.level >= maximumLevel ? null : (experienceByTargetLevel[character.level] ?? null);
}

export function getQuestExperienceGain(percentages: number[], character: Character) {
  const percentage = percentages[character.level - 1] ?? 0;
  const requiredExperience = getExperienceRequiredForNextLevel(character);
  return requiredExperience && percentage > 0
    ? Math.max(0, Math.floor((requiredExperience * percentage) / 100))
    : 0;
}

export function applyCharacterExperience(character: Character, experienceGain: number) {
  let exp = Math.max(0, Math.floor(character.exp + experienceGain));
  let level = character.level;
  let requirement = getExperienceRequiredForNextLevel({ ...character, level });

  while (requirement !== null && exp >= requirement) {
    exp -= requirement;
    level += 1;
    requirement = getExperienceRequiredForNextLevel({ ...character, level });
  }

  if (requirement === null) exp = 0;
  return { exp, level };
}
